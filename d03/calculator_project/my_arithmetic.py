#!/usr/bin/env python3
#----------------------------------------------------------------------
def add(x, y):
    """"""
    return x + y

#----------------------------------------------------------------------
def division(x, y):
    """"""
    if y == 0:
        raise Exception("can't divide by 0")
    else:
        return x / y

#----------------------------------------------------------------------
def multiply(x, y):
    """"""
    return x * y

#----------------------------------------------------------------------
def subtract(x, y):
    """"""
    return x - y